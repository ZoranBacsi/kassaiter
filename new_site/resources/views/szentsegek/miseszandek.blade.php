<h1>Miseszándék</h1>
<p>
    Misék után a sekrestyében, vagy a plébániai hivatalban (nyitvatartási időben) lehet előjegyezni, a mise tiszteletdíjának megfizetésével egyidőben.
    Valakinek tiszteletére misézni csak akkor lehet, ha az Egyház a szentek vagy legalábbis a boldogok közé sorolta.
    Másoknak csak a lelki üdvéért lehet szentmisét fölajánlani. Ez még akkor is így van, ha a szóbanforgó szentmisén az illető személy tisztelői gyűltek össze.
    Csak olyan kérésért szabad misézni, amely Isten üdvösségtervébe beleillik.
</p>