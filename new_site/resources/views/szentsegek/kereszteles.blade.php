<ol>
    <li>Köszöntés,</li>
    <li>Pap: Mi a gyermek neve?</li>
    <li>Szülők: N.</li>
    <li>Pap: A gyermek védőszentje tehát Szent ….. lesz.</li>
    <li>Pap: Mit kértek Isten egyházától?</li>
    <li>Szülők: A keresztséget.</li>
    <li>Pap: <span class="underline">Nektek, akik gyermeketek számára a keresztséget kéritek kötelességtek lesz őt úgy nevelni, hogy hitünk szerint éljen.
        Tartsa meg Isten parancsait: úgy szeresse Istent és embertársait, ahogy Krisztus Urunk tanította. Vállaljátok-e ezt a kötelezettséget?</span></li>
    <li>Szülők: <strong>Vállaljuk.</strong></li>
    <li>Ezután a pap, a szülők, keresztszülők a kereszt jelével jelölik meg a gyermek homlokát.</li>
    <li>Az Evangélium olvasása</li>
    <li>Könyörgések a gyermekért, szülőkért, keresztszülőkért. Válasz: Kérünk téged, hallgass meg minket.</li>
    <li>Isten szentjeinek közbenjárását kérve válaszoljuk: Könyörögj / könyörögjetek érettünk!</li>
    <li>Imádság a gonosz lélek hatalmának megtöréséért. birkózókat bekenték olajjal, hogy kicsússzanak egymás kezéből, a keresztelendőt megkenjük olajjal, hogy kicsússzon a sátán hatalmából.</li>
    <li>A víz megáldása. A család válasza: Áldunk téged Istenünk.</li>
    <li>Hitünk megvallása</li>
    <li>Pap: Akarjátok-e, hogy N. elnyerje a keresztséget az Egyház hitében, amelyet az imént mindnyájan megvallottunk?</li>
    <li>Család: Akarjuk!</li>
    <li>A gyermek fejét szabaddá tesszük, és következik a keresztelés. <br /> Pap: <strong>N., én megkeresztellek téged, az Atya, és a Fiú, és a Szentlélek nevében.</strong></li>
    <li>Keresztség utáni megkenés, amely által a megkeresztelt részesedik Krisztus papságában, prófétaságában és királyságában.</li>
    <li>A fehér ruha átadása. Az első keresztelések húsvétkor voltak. A megkereszteltek –lelkük tisztaságának jeleként egy hétig viselték a fehér ruhát, amelyet csak a következő vasárnap vetettek le.
    Az Egyház ezt a vasárnapot máig Fehérvasárnapnak hívja.</li>
    <li>Az égő gyertya átadása. Az égő gyertya Krisztus világosságára emlékeztet bennünket, aki a nemzetek világossága.
    A megkeresztelt Krisztust öltötte magára, és részesedett ebből a világosságból.</li>
