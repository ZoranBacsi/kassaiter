		<b>Rózsafüzér ígéretek</b>
		<ol>
			<li>Aki a rózsafüzér imádkozásával hűségesen szolgál, jelentős kegyelmekben fog részesülni.</li>
			<li>Különleges védelmem és legnagyobb kegyelmeket ígérem azoknak, akik a rózsafüzért imádkozzák.</li>
			<li>A rózsafüzér erőteljes védelem lesz a pokollal szemben. Felőrli az erkölcstelenséget, csökkenti a bűnt, és legyőzi az eretnekséget.</li>
			<li>Erényességet és jó munkát nyújt a gyarapodáshoz; a lélek bőséges isteni irgalomban fog részesülni; az emberek szívét visszahúzza a világ hiúságainak szeretetétől, és örökkévaló dolgok iránti vágyakozásra emeli. A lelkek ezáltal megtisztulnak.</li>
			<li>Az a lélek, amelyik a rózsafüzér imádkozásával rám bízza magát, nem fog elveszni.</li>
			<li>Aki őszintén imádkozza a rózsafüzért, fontolóra veszi és alkalmazza magára a szent hittitkokat, azt sosem győzi le a szerencsétlenség. Isten nem fogja büntetni az igazságszolgáltatásakor, nem fognak elveszni egy felkészületlen halállal; ha igazságosak, akkor Isten kegyelmében maradnak és méltók lesznek az örök életre.</li>
			<li>Aki igaz áhítattal imádkozza a rózsafüzért, nem fog az Egyház oltáriszentsége nélkül meghalni.</li>
			<li>Akik hűen imádkozzák a rózsafüzért, azok életükben és halálukban Isten világosságában és bőséges kegyelmében lesznek; haláluk pillanatában pedig a paradicsomban a szentek érdemében részesülnek.</li>
			<li>A tisztítótűztől megszabadítom azokat, akik hűek voltak a rózsafüzérhez.</li>
			<li>A rózsafüzér hűséges gyermekei nagy dicsőségben fognak részesülni a mennyben.</li>
			<li>Mindent meg fogsz kapni, amit a rózsafüzér imádkozásakor kérsz tőlem.</li>
			<li>Akik terjesztik a szent rózsafüzért, azokat szükségükben segíteni fogom.</li>
			<li>Isteni fiamnál elértem, hogy a rózsafüzér pártolóinak életükben és haláluk órájában közbenjárójuk lesz a teljes égi törvényszéken.</li>
			<li>Akik imádkozzák a rózsafüzért, azok gyermekeim, és egyetlen fiamnak, Jézus Krisztusnak testvérei.</li>
		</ol>
