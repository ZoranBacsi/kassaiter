<h1>Házasságkötés</h1>
<p>
    A házasságkötést jegyesoktatás előzi meg, így az esküvő tervezett időpontja előtt szükséges legalább 3-4 hónappal a plébánia irodában jelentkezni (évente egy jegyesoktatás van tavaszi kezdettel, így esetenként még előbb kell jelentkezni).
</p>
<ol>
    <li>Keresztlevelek (menyasszony és vőlegény) - a keresztelés helye szerinti plébánia állítja ki (evangélikus és református fél esetén is szükséges) - az esküvő előtt legfeljebb három hónappal lehet kikérni.</li>
    <li>Házassági elbocsájtó - az állandó lakcím szerint illetékes plébánia állítja ki (kiállításhoz be kell mutatni ott a keresztleveleket!)</li>
    <li>Anyakönyvezéshez szükséges ADATLAP kitöltése</li>
</ol>