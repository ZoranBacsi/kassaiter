<h1>Keresztelés</h1>
A (gyermek)keresztelés előtt legalább 1 hónappal a plébánia irodában lehet jelentkezni, az időpont egyeztetésére is ekkor kerülhet sor.<br />
Minden gyermeket a lakóhely szerinti plébánián kell megkeresztelni. Ha nem ott szeretnék a liturgiát, akkor „keresztelési elbocsátót” kell hozni az illetékes plébánostól.<br />
Csak a szülők, vagy egyik szülő kérheti. Rokonok, barátok, ismerősök, nagymamák, gyermekvigyázók nem.<br />
Ki kell tölteni egy jelentkezési lapot.<br />
Egy lelkipásztorral el kell beszélgetni: Miért is kérik a keresztséget gyermekük számára?<br />
Szükség van továbbá keresztszülőre.<br />
Valamint egy védőszentre. (<a target="_blank" href="http://www.katolikus.hu/szentek/">Segítség a védőszent nevének választásához</a>)<br /><br />
