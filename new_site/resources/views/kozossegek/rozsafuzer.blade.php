    <b><i>„Imádkozzátok a rózsafüzért – szívvel!”</i></b><br><br><br>

	<b>Társulatunk minden héten kedden közös imaórát tart 17:30-tól 18:30-ig. Ezt követően 18:30-tól szentmise van a Rózsafüzér Társulat élő és elhunyt tagjaiért.</b><br><br><br>

	<p>
		A Rózsafüzér Társulat a legelterjedtebb testvérület az egész Egyházban. Alapítójának Szt. Domonkost tartják, aki 1211-ben Toulouse-ban szervezte meg az első Rózsafüzér Társulatnak tekinthető társulatot. Az első társulatot, mely biztosan Rózsafüzér Társulat volt, 1470-ban Douaiban (Fro.) Alanus de Rupe alapította.<br><br>
		A rózsafüzér ősi formáját a szerzetesek és remeték már az első időktől kezdve használták, de elterjesztéséért különösen a ciszterciek és a domonkosok tettek sokat Mária tiszteletük által. Elnevezése egy 15. századi legendából származik, mely szerint egy Máriát igen tisztelő léleknek az volt a szokása, hogy rózsából koszorút kötött és ezzel díszítette a Szűzanya szobrát. Egy napon látomásában azt a figyelmeztetést kapta, hogy van más "rózsakoszorú" is, amelynek jobban örül a Szűzanya: 50 Üdvözlégy elmondása, mert az imádság rózsává lesz az Istenanya kezében, aki ezekből a legszebb rózsakoszorút fonja magának. Innen a rózsafüzér elnevezés. Mi magyarok a rózsafüzért legtöbbször szentolvasónak mondjuk, mely az Üdvözlégyek számlálására "olvasására" utal.<br><br>
		<b>Célja</b> tagjainak a Rózsafüzér imádkozásával való megszentelése. A tagok a keresztény testvériség jegyében egyformán részesednek a Rózsafüzér Társulat lelki javaiból.<br><br>
		<b>Tagja</b> lehet minden katolikus keresztény, a felvétel a tagsági könyvbe való beírással történik. A tagok vállalják, hogy hetenként egyszer elimádkozzák a teljes rózsafüzért, illetve, hogy naponta egyszer elimádkoznak egy tized rózsafüzért.<br>
		A tagok úgynevezett rózsacsokrokat alkotnak. Egy csokor 20 tagból áll. A 20 buzgó lélek, aki komolyan megígéri és meg is valósítja, hogy naponta egyetlenegy tizedet elimádkozik. Legfontosabb, hogy minden 20 imádkozó között legyen egy vezető, aki szívén viseli, hogy mindenkinek legyen saját titka, amit abban a hónapban imádkozik. Az Élő Rózsafüzér Társulat legnagyobb lelki haszna talán az, hogy az a 20 tag, akinek mindegyike naponta egy picinyke tizedet elimádkozik, amelyik abban a hónapban neki jutott a sorsolásnál, mindegyikük részesül az egész 20 tized elimádkozásának kegyelmeiben. A 20 lélek imája összeáll egy egységgé, egy családdá. Összenőnek lelki csokorrá, eggyé válik imájuk. Minden egyes tag elnyeri a teljes rózsafüzér elimádkozásával járó teljes búcsút.<br>
		A kassai téri Társulatunkhoz jelenleg 2 csokor tartozik. Megalakulása óta ez a Társulat töretlen hűségével, kitartásával, szeretetével biztos ima-hátteret jelent egyházközségünknek.<br><br>

		<b>Társulatunk minden héten kedden közös imaórát tart 17:30-tól 18:30-ig. Ezt követően 18:30-tól szentmise van a Rózsafüzér Társulat élő és elhunyt tagjaiért</b>, így azok, akik a Rózsafüzér Társulat tagjai közé tartoznak biztosak lehetnek benne, hogy valaki mindig fog értük imádkozni.<br><br>
		<b>Társulatunk tagjai minden hónap első keddjén cserélik ki a „titkokat” egymás között.</b><br><br>
		<b>A rózsafüzér imádkozása</b><br><br>
		A rózsafüzér az ismétlődő imádság legkedveltebb formája, melynek végzésével bárki megteheti, amit az Úr Krisztus mondott: "szüntelenül kell imádkozni és nem szabad belefáradni" (Lk 18,1; vö. 21,36). A rózsafüzérben a Miatyánk közbeiktatásával 20-szor 10 Üdvözlégyet imádkozunk, s a tizedekhez hozzákapcsoljuk megváltónk Jézus Krisztus földi életének misztériumát. A titkokban Jézus életének a legfontosabb eseményei tárulnak elénk: fogantatása és gyermekkora, tanítói működése, szenvedése, dicsőséges feltámadása, az Egyház születése, Szűz Mária megdicsőülése. A rózsafüzér imádságban Mária vezet bennünket közelebb Jézushoz.<br><br>
