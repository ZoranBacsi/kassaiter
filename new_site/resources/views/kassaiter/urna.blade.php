<h1>Urnatemető</h1>
<p>Az altemplomunkban lévő urnatemető urnafülkéit a templomunkban rendszeresen vallásukat gyakorló hívő családjainknak tartjuk fent. A vallásukat más plébániákon gyakorló hívőknek lehetőségük van szórótemetés kérésére. A hamvak itt egy közös urnába kerülnek, melyet altemplomunk oltárában alakítottunk ki.
Érdeklődni irodai időben illetve csütörtökön délelőtt 10-13 óráig lehet a plébánia irodájában.
<h4>Nyitvatartás:</h4>
Az urnatemető a misék kezdete előtt 20 perccel és a szertartások végéig látogatható.
<br><br>
Minden hónap első szombatján Urnatemetőnk halottjaiért ajánljuk fel a szentmisét.
<br><br>
<strong>Mivel a miserendben (elsősorban ünnepek idején) előfordulhatnak változások, ezért kérjük az aktuális hirdetéseket (kezdőlapon) is vegye figyelembe.
A misék ideje alatt a sekrestye ajtaja zárva van (csak az urnatemető közelíthető meg). Kérjük, hogy csengetéssel, kopogással a szertartást ilyenkor ne zavarják.</strong></p>
