<h1>Temetés</h1>
<p>Elveszíteni valakit, akit szeretünk - ez az emberi lét egyik legfájdalmasabb tapasztalata.
    Életünk során szinte mindnyájan szembesülünk hozzátartozóink elvesztésével.
    Ugyanakkor Krisztus feltámadása a keresztény ember számára reményt adott, hogy ahová Ő, a Fő eljutott, bennünket is vár.
    <em>„Ha aztán elmegyek, és helyet készítek nektek, újra eljövök, és magammal viszlek benneteket, hogy ti is ott legyetek, ahol én vagyok.”</em> (Jn 14,3)
    Így szerettünk elvesztése, mégha fájdalmas is, nem végleges. Így számunkra a halál többé nem elmúlás, hanem búcsú a viszontlátás reményében.
    <br>
    <br>
    Az egyház a gyászolók fájdalmát a méltó temetési szertartás elvégzésével Krisztus örök életet adó ígéretéhez kapcsolja.
    <br>
    <br>
    Az egyházi szertartás kérésének feltétele, hogy az elhunyt katolikus hívő legyen, plébániánk területén lakjon és a hozzátartozók kérik az egyház imádságát az elhunytért.
    <br>
    <br>
    Temetés bejelentése a plébánia irodában lehetséges.
</p>

