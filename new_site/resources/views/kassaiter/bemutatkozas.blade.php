<h1>Bemutatkozás</h1>
<br>
<p>
    Plébániánk Budapest XIV. kerületének azon a részén található, ahol sem közlekedési csomópont,
    sem egyéb technikai ösztönző erő nem segíti elő a templomi életben való tömeges részvételt.
    Akik ide járnak, azok vagy a közösséget, vagy a gyönyörű templom sajátos légkörét, vagy a liturgia felemelő erejét keresik,
    amely sohasem sietős és egy ragyogó ministráns-csapat közreműködése emeli ki a megjelenő misztériumot.
    <br><br>
    Plébániánk mozgató rugói azok a közösségek, amelyek összetartásukkal,
    elmélyültebb lelki életükkel és gyakorlati segítségükkel egészítik ki lelkipásztoraink munkáját.
    Ők szervezik rendezvényeinket, kirándulásainkat, lelkigyakorlatainkat, ők segítik karitatív életünket.
    Plébániai közösségünk igyekszik az élet minden hasznos területén is híveink rendelkezésére állni.
    Családjainkat és magányos testvéreinket orvosi kapcsolatokkal, a gyermek-egészségügyi alap működtetésével,
    különféle termelőkkel való kapcsolatba-hozással (méz-termelő, optikus) is segítjük.
    Ugyanakkor évek óta folynak azok a kulturális esemény-sorozatok, melyeknek irodalmi részét a
    <a href="http://www.keesz.hu/">Keresztény Értelmiségiek Szövetség</a>ének helyben megalakult zuglói szervezete,
    míg koncertsorozatait orgonaművész-kántorunk,
    <a href="http://www.myorganmusic.com/Orgonamuvesz/Gesztest-Toth_Laszlo.html">Gesztesi Tóth László</a> szervezi.
    <br><br>
    Nagyjából a többi plébániához hasonló missziós kötelezettségeinket teljesítjük, helyi sajátságunk azonban,
    hogy a hozzánk tartozó, városligeti Jáki kápolna az ország legkeresettebb eskető helye. Évente száz körüli jegyespár köti itt össze az életét,
    többségükben a vallási élet kezdetén járó fiatalokról van szó, akiknek a szentségre való felkészítése ezért komolyabb odafigyelést igényel.
    <br><br>
    <strong>A plébánia-épület a templom főbejáratával szemben, a park másik oldalán, a Kassai tér 2. szám alatt található.</strong>
</p>